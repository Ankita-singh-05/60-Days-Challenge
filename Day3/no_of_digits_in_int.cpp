//Number of digits in an integer

#include<iostream>
using namespace std;

int main(){

    int num;
    cout<<"Enter any number:"<<endl;
    cin>>num;

    for(int i=0; i<=num; i++){
        int digit = num[i];
    }
    cout<<"Number of digits in an integer "<<num<<" is "<<digit<<endl;

    return 0;
}
